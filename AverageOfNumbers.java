public class AverageOfNumbers 
{
    public static void main(String args[]) 
    {
        int[] numArray = { 45, 65,34,34,31,56};
        double sum = 0.0;

        for (double num: numArray) {
           sum += num;
        }

        double average = sum / numArray.length;
        System.out.format("The average is: %.2f", average);
    }
    
}
